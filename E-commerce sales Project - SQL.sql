CREATE TABLE customers (
    customer_id INT,
    customer_name VARCHAR(100),
    email VARCHAR(100),
    city VARCHAR(50),
    country VARCHAR(50),
    loyalty_status VARCHAR(20)
);

describe customers;

CREATE TABLE products (
    product_id INT,
    product_name VARCHAR(100),
    category VARCHAR(50),
    price FLOAT,
    stock_quantity INT
);

describe products;

CREATE TABLE orders (
    order_id INT,
    customer_id INT,
    product_id INT,
    order_date DATETIME,
    quantity INT,
    total_amount FLOAT,
    payment_method VARCHAR(50),
    order_status VARCHAR(50)
);

describe orders;

